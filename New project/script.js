function toTitleCase(value) {
  return value
    .split(" ")
    .map((word) => word[0].toUpperCase() + word.slice(1))
    .join(" ");
}

function normalizeBudgetLower(budgetText) {
  const match = budgetText.match(/(\d+(?:\.\d+)?)k/i);
  return match ? Number(match[1]) : Number.POSITIVE_INFINITY;
}

function getBudgetBucket(budgetText) {
  const amount = normalizeBudgetLower(budgetText);
  if (amount <= 10) return "low";
  if (amount <= 13) return "mid";
  return "high";
}

function getFilteredAndSorted(list) {
  const pricingValue = pricingFilter.value;
  const categoryValue = categoryFilter.value;
  const sortValue = sortSelect.value;

  let output = [...list];

  if (pricingValue !== "all") {
    output = output.filter((pg) => getBudgetBucket(pg.budget) === pricingValue);
  }

  if (categoryValue === "girls-only") {
    output = output.filter((pg) => pg.girlsOnly);
  }

  output.sort((a, b) => {
    if (sortValue === "distance") {
      const aDist = typeof a.distanceKm === "number" ? a.distanceKm : normalizeBudgetLower(a.budget);
      const bDist = typeof b.distanceKm === "number" ? b.distanceKm : normalizeBudgetLower(b.budget);
      return aDist - bDist;
    }
    if (sortValue === "budget") return normalizeBudgetLower(a.budget) - normalizeBudgetLower(b.budget);
    return b.rating - a.rating;
  });

  return output;
}

function toBudgetLabel(priceValue) {
  const amount = Number(priceValue || 0);
  const lowerK = Math.max(1, Math.round(amount / 1000));
  return `${lowerK}k - ${lowerK + 1}k`;
}

function slugify(value) {
  return String(value || "")
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

function createScoreSet(pg) {
  const base = Math.round((Number(pg.rating || 4) / 5) * 100);
  return {
    foodQuality: Math.max(68, Math.min(98, base + (pg.highlights.includes("Meals") ? 5 : -2))),
    safety: Math.max(70, Math.min(99, base + (pg.girlsOnly ? 7 : 3))),
    comfort: Math.max(72, Math.min(97, base + (pg.highlights.includes("AC") ? 6 : 2))),
    valueForMoney: Math.max(65, Math.min(96, 96 - Math.round((Number(pg.price || 0) - 6500) / 180)))
  };
}

function getCollegeDisplayName(collegeKey) {
  if (!collegeKey) return "Your Campus";
  return toTitleCase(String(collegeKey).replace(/^owner::/, "").replaceAll("-", " "));
}

function buildPGDescription(pg, collegeKey) {
  return `${pg.name} is a student-focused stay option near ${getCollegeDisplayName(collegeKey)} with ${pg.highlights
    .slice(0, 3)
    .join(", ")
    .toLowerCase()} and a ${pg.rating}/5 student rating.`;
}

function enrichPG(pg, collegeKey, typedQuery = "") {
  const activeCollegeKey = pg.collegeKey || collegeKey;
  const campusName = typedQuery ? toTitleCase(typedQuery) : getCollegeDisplayName(activeCollegeKey);
  const scoreSet = createScoreSet(pg);
  const gallery = [pg.image, ...DEFAULT_PG_GALLERY].slice(0, 3);
  const pgId = `${activeCollegeKey}::${pg.name}`;

  return {
    ...pg,
    pgId,
    collegeKey: activeCollegeKey,
    slug: slugify(`${pg.name}-${activeCollegeKey}`),
    campusName,
    ownerPhone: pg.ownerPhone || "+91 98765 43210",
    description: pg.description || buildPGDescription(pg, activeCollegeKey),
    scoreSet,
    gallery
  };
}

